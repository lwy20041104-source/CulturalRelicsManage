    COUNT(DISTINCT image_id) as unique_images
FROM relic_image_relation;

-- 查看前10条关联记录
SELECT 
    r.id,
    r.relic_id,
    cr.relic_name,
    r.image_id,
    i.image_name,
    r.relation_type,
    r.create_time
FROM relic_image_relation r
LEFT JOIN cultural_relic cr ON r.relic_id = cr.id
LEFT JOIN image_library i ON r.image_id = i.id
ORDER BY r.id
LIMIT 10;

